/**
 * `Translation` is derived from `zh` (single source of truth): literal string
 * leaves are widened to `string` so `en: Translation` can supply its own values
 * without literal-type conflicts, while the key structure stays in sync with
 * `zh` automatically. Add a key in `zh` and `en` is forced to match (TS error
 * if missing/extra), and `NestedKeyOf<Translation>` picks it up for `t()`.
 */
type Stringify<T> = {
  readonly [K in keyof T]: T[K] extends string ? string : Stringify<T[K]>;
};

export type Translation = Stringify<typeof zh>;
